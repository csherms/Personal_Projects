import turtle

casey_turtle = turtle.Turtle()

def makeSquare():
  casey_turtle.forward(100)
  casey_turtle.right(90)
  casey_turtle.forward(100)
  casey_turtle.right(90)
  casey_turtle.forward(100)
  casey_turtle.right(90)
  casey_turtle.forward(100)

makeSquare()
casey_turtle.forward(100)
makeSquare()